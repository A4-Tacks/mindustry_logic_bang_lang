#[test]
fn anon_name_test() {
    let nums = [
        (0, "0"),
        (1, "1"),
        (9, "9"),
        (10, "10"),
        (19, "19"),
        (26, "26"),
        (33, "33"),
        (40, "40"),
        (99, "99"),
        (100, "100"),
        (124, "124"),
        (125, "125"),
        (134, "134"),
        (160, "160"),
        (999, "999"),
        (1000, "0G8"),
        (1002, "0GA"),
        (1028, "0Ga"),
        (1030, "0Gc"),
        (1030, "0Gc"),
        (1054, "0H0"),
        (3843, "0zz"),
        (3844, "0100"),
        (3845, "0101"),
        (3854, "010A"),
    ];
    for (num, target) in nums {
        let fmtted = super::gen_anon_name(num, |x| x.to_string());
        assert_eq!(fmtted, target);
    }
}
